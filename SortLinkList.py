# Write a program to sorting a linear linked list in ascending order.
class Node:
    def __init__(self, data=None):
        self.data = data
        self.next = None

class LinkedList:
    def __init__(self):
        self.head = None

    def append(self, data):
        new_node = Node(data)
        if not self.head:
            self.head = new_node
            return
        current = self.head
        while current.next:
            current = current.next
        current.next = new_node

    def print_list(self):
        current = self.head
        if current is None:
            print ('Empty List')
        else:
            while current:
                print(current.data,'-->',end= ' ')
                current = current.next